package br.com.mtanuri.java;

public class Calculadora {

    public static int soma(int num1, int num2) {
        return num1 + num2;
    }

    public static int subtrai(int i, int j) {
        return i  -j;
    }

    public static int divide(int i, int j) {
        return i/j;
    }

    public static int multiplica(int i, int j) {
        return i*j;
    }

    public static double raiz(int i) {
        return Math.sqrt(i);
    }
    
}